// //Question 118: Write a loop that logs numbers from 1 to 10 to the console.


// Initialize a variable named i to 1
// This is the starting point of our loop
let i = 1;

// Loop will run as long as i is less than or equal to 10
// After each iteration, i will be incremented by 1
for (let i = 1; i <= 10; i++) {
    // Output the current value of i to the console
    console.log(i);
}
