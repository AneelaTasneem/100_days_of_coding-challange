//Question 136: Use console.log() to debug and track the value of a variable inside a loop.

// Start a for loop with variable i starting at 0
// The loop will continue as long as i is less than 5
// After each iteration, i will increase by 1
for (let i = 0; i < 5 ; i++) {
    // Inside the loop, log a message to the console
    // The message includes the text "The value of i is:" 
    // followed by the current value of i
    console.log("The value of i is: " + i);
}
