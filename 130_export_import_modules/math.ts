// math.ts

// Exporting a function named 'add' that takes two numbers (a and b) as arguments
// and returns their sum.
export function add(a: number, b: number): number {
    return a + b;
}

// Exporting a function named 'subtract' that takes two numbers (a and b) as arguments
// and returns the result of subtracting b from a.
export function subtract(a: number, b: number): number {
    return a - b;
}
